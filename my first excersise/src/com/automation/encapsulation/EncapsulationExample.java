package com.automation.encapsulation;

public class EncapsulationExample {
	
	// pojo (Plain old java object)
	// java Bean
	
	private int a;
	private String str;
	
	public int getA() {
		return a;
	}
	public void setA(int a) {
		this.a = a;
	}
	
	
	public String getStr() {
		return str;
	}
	public void setStr(String str) {
		this.str = str;
	}
	

}
